export interface Character {
  id: string;
  /** キャラ名 */
  name: string;
  /** 肩書 */
  title: string;
  /** 選択画面での紹介文 */
  description: string;
  /** 選択画面での決めゼリフ */
  catchphrase: string;
  /** テーマカラー (hex) */
  color: string;
  /** お題の入力例 */
  topicPlaceholder: string;
  /** お題の例。自動生成のfew-shotと生成失敗時のフォールバックを兼ねる */
  topicExamples: string[];
  /** 性格・演技指導(システムプロンプトに埋め込む) */
  personality: string;
  /** VOICEVOX の話者(スタイルID)とキャラ基本の調子 */
  voice: CharacterVoice;
}

export interface CharacterVoice {
  /** VOICEVOX のスタイルID(GET /speakers で確認) */
  speaker: number;
  /** 話速の基本値(既定 1.0) */
  speedScale?: number;
  /** ピッチの基本値(既定 0.0) */
  pitchScale?: number;
}

